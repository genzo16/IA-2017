package model;

public class Diagnostico {
	
	Long id;
	String enfermedad;
	String zonaTratar;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getEnfermedad() {
		return enfermedad;
	}
	public void setEnfermedad(String enfermedad) {
		this.enfermedad = enfermedad;
	}
	public String getZonaTratar() {
		return zonaTratar;
	}
	public void setZonaTratar(String zonaTratar) {
		this.zonaTratar = zonaTratar;
	}
	
	

}
