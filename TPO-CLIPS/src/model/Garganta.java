package model;

public class Garganta {
	
	Long id;
	String estado;
	String amigdalas;
	String estudioMedico;
	String estudioAdicional;
	String observaciones;
	String infeccion;
	String examinacionAdicional;
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getAmigdalas() {
		return amigdalas;
	}
	public void setAmigdalas(String amigdalas) {
		this.amigdalas = amigdalas;
	}
	public String getEstudioMedico() {
		return estudioMedico;
	}
	public void setEstudioMedico(String estudioMedico) {
		this.estudioMedico = estudioMedico;
	}
	public String getEstudioAdicional() {
		return estudioAdicional;
	}
	public void setEstudioAdicional(String estudioAdicional) {
		this.estudioAdicional = estudioAdicional;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	public String getInfeccion() {
		return infeccion;
	}
	public void setInfeccion(String infeccion) {
		this.infeccion = infeccion;
	}
	public String getExaminacionAdicional() {
		return examinacionAdicional;
	}
	public void setExaminacionAdicional(String examinacionAdicional) {
		this.examinacionAdicional = examinacionAdicional;
	}
	
	

}
